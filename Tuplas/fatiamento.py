tupla = ("f", "l", "a", "v", "i", "o",)

print(tupla[2:])  # o resultado será: ('a', 'v', 'i', 'o')
print(tupla[:2])  # o resultado será: ('f', 'l')
print(tupla[1:3])  # o resultado será: ('l', 'a')
print(tupla[0:3:2])  # o resultado será: ('f', 'a')
print(tupla[::])  # o resultado será: ('f', 'l', 'a', 'v', 'i', 'o')
print(tupla[::-1])  # o resultado será: ('o', 'i', 'v', 'a', 'l', 'f')